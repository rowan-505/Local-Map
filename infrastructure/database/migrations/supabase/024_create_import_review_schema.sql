-- =============================================================================
-- Supabase migration 024: import_review workspace + system publish alignment
-- =============================================================================
--
-- Purpose:
--   - import_review.* is a remote review/edit workspace only — not production core.
--   - review_overrides holds validator/dashboard corrections atop normalized_data.
--   - Promotion into core happens only via system.system_publish_batches/items (future API).
--   - Local raw/tmp_import remain on the pipeline host; Supabase receives review-ready rows.
--
-- Safety:
--   - Non-destructive: no DROP of core/ref/tiles/app_auth objects.
--   - Extends existing system.* tables from 021/022 via ADD COLUMN IF NOT EXISTS.
--   - Guarded DO blocks for FKs and unique constraints (skip with NOTICE on conflict).
--   - PK style matches 021/022: bigint generated by default as identity (equivalent to BIGSERIAL).
--
-- RLS / auth:
--   - TODO: import_review and system publish endpoints must be admin-only at the API layer.
--   - RLS is not enabled here; enable + policies only if Supabase clients access these tables
--     directly with non-service roles.
--
-- Apply: Supabase SQL Editor (paste full file). Do not run from CI without review.
--
-- =============================================================================

