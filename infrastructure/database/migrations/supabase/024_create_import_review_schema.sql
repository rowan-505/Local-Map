create table if not exists import_review.review_batches (
    id bigint generated by default as identity primary key,
    public_id uuid not null default gen_random_uuid(),
    batch_name text not null,
    source_snapshot_version text not null,
    source_snapshot_id_local bigint null,
    source_registry_code text null,
    region_code text null,
    boundary_id_local bigint null,
    boundary_code text null,
    entity_families text[] not null default '{}'::text[],
    status text not null default 'uploaded',
    upload_mode text not null default 'local_pipeline',
    total_candidate_count integer not null default 0,
    uploaded_candidate_count integer not null default 0,
    preserved_reviewed_count integer not null default 0,
    skipped_count integer not null default 0,
    summary jsonb not null default '{}'::jsonb,
    uploaded_by bigint null,
    uploaded_at timestamptz not null default now(),
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint review_batches_status_chk check (
        status in (
            'uploaded',
            'reviewing',
            'review_completed',
            'publish_batch_created',
            'promoted',
            'failed',
            'archived'
        )
    ),
    constraint review_batches_upload_mode_chk check (btrim(upload_mode) <> '')
);

create index if not exists review_batches_source_snapshot_version_idx
    on import_review.review_batches (source_snapshot_version);

create index if not exists review_batches_region_code_idx
    on import_review.review_batches (region_code);

