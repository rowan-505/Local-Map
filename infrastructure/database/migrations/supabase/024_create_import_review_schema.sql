create or replace view import_review.v_ready_for_publish_summary as
select
    b.id as review_batch_id,
    b.public_id as batch_public_id,
    b.batch_name,
    coalesce(x.ready_cnt, 0)::bigint as rows_ready_or_batched_or_approved,
    coalesce(x.promoted_cnt, 0)::bigint as rows_promotion_status_promoted
from import_review.review_batches b
left join lateral (
    select
        sum(
            case
                when c.promotion_status in ('ready', 'batched') then 1
                when c.promotion_status = 'not_ready'
                    and coalesce(c.review_decision, '') = 'approved'
                    and c.review_status = 'approved' then 1
                else 0
            end
        ) as ready_cnt,
        sum(case when c.promotion_status = 'promoted' then 1 else 0 end) as promoted_cnt
    from (
        select promotion_status, review_decision, review_status from import_review.building_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.place_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.road_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.address_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.admin_area_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.bus_stop_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.bus_route_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.bus_route_variant_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.bus_route_stop_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.landuse_candidates c
            where c.review_batch_id = b.id
        union all
        select promotion_status, review_decision, review_status from import_review.water_line_candidates c
